#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	int		i;
	char	*a;

	i = 0;
	if (n == 0)
		return (NULL);
	a = s;
	while (n--)
		*(a++) = 0;
	return (s);
}
